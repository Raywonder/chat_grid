def _matching_seed_exists(items: dict[str, WorldItem], seed: SeedItem) -> bool:
    """Return true if this built-in seed is already represented."""

    if seed.id and seed.id in items:
        return True
    seed_title = seed.title.casefold()
    for item in items.values():
        if item.type != seed.type:
            continue
        if item.locationId != seed.location_id:
            continue
        if item.title.casefold() == seed_title:
            return True
    return False


def build_seed_item(seed: SeedItem, *, now_ms: int) -> WorldItem:
    """Create one WorldItem from seed metadata and catalog defaults."""

    definition = get_item_definition(seed.type)
    params = deepcopy(definition.default_params)
    params.update(deepcopy(seed.params))
    return WorldItem(
        id=seed.id or str(uuid.uuid4()),
        type=seed.type,
        title=seed.title,
        locationId=seed.location_id,
        x=seed.x,
        y=seed.y,
        createdBy="system",
        createdByName="system",
        updatedBy="system",
        updatedByName="system",
        createdAt=now_ms,
        updatedAt=now_ms,
        version=1,
        capabilities=list(definition.capabilities),
        useSound=definition.use_sound,
        emitSound=definition.emit_sound,
        params=params,
        carrierId=None,
    )


def ensure_builtin_items(items: dict[str, WorldItem], *, now_ms: int) -> list[WorldItem]:
    """Insert/update built-in service/station items and return changed items."""

    changed: list[WorldItem] = []
    for item_id in DEPRECATED_BUILTIN_ITEM_IDS:
        item = items.get(item_id)
        if item is not None and item.createdBy == "system":
            del items[item_id]
    for seed in BUILTIN_WORLD_ITEMS:
        existing = items.get(seed.id) if seed.id else None
        if existing is not None and existing.createdBy == "system":
            if (
                existing.type != seed.type
                or existing.locationId != seed.location_id
                or existing.x != seed.x
                or existing.y != seed.y
            ):
                replacement = build_seed_item(seed, now_ms=now_ms)
                replacement.createdAt = existing.createdAt
                replacement.version = existing.version + 1
                items[replacement.id] = replacement
                changed.append(replacement)
                continue
            updated = False
            preserve_existing_params = existing.updatedBy not in {"", "system", None}
            if existing.title != seed.title and not preserve_existing_params:
                existing.title = seed.title
                updated = True
            for key, value in seed.params.items():
                server_managed_tv_metadata = key in {"tvLibrarySources", "tvProviderSources"}
                if key not in existing.params:
                    existing.params[key] = deepcopy(value)
                    updated = True
                elif (
                    not preserve_existing_params or server_managed_tv_metadata
                ) and existing.params.get(key) != value:
                    existing.params[key] = deepcopy(value)
                    updated = True
            if updated:
                existing.updatedAt = now_ms
                existing.updatedBy = "system"
                existing.updatedByName = "system"
                existing.version += 1
                changed.append(existing)
            continue
        if _matching_seed_exists(items, seed):
            continue
        item = build_seed_item(seed, now_ms=now_ms)
        items[item.id] = item
        changed.append(item)
    return changed
