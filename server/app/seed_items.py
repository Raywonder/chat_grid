def furniture_params(
    *,
    kind: str,
    material: str = "wood",
    style: str = "warm home",
    supports_objects: bool = True,
    surface_slots: int = 0,
    seating_capacity: int = 0,
    posture_mode: str = "none",
    surface_note: str = "",
) -> dict[str, str | int | bool]:
    """Return complete seed params for one furniture item."""

    return {
        "furnitureKind": kind,
        "material": material,
        "style": style,
        "condition": "good",
        "supportsObjects": supports_objects,
        "surfaceSlots": surface_slots,
        "seatingCapacity": seating_capacity,
        "postureMode": posture_mode,
        "surfaceNote": surface_note,
    }


def house_object_params(
    *,
    kind: str,
    placement: str,
    material: str = "mixed",
    fragility: str = "normal",
    repair_cost: int = 8,
    purchase_cost: int = 14,
    replacement_hint: str = "A similar everyday replacement would work.",
    giftable: bool = True,
    description: str = "A small household object.",
    surface_id: str = "",
    surface_title: str = "",
    **extra: object,
) -> dict[str, object]:
    """Return complete seed params for one household object."""

    params: dict[str, object] = {
        "objectKind": kind,
        "placement": placement,
        "material": material,
        "fragility": fragility,
        "condition": "intact",
        "repairCost": repair_cost,
        "purchaseCost": purchase_cost,
        "replacementHint": replacement_hint,
        "giftable": giftable,
        "description": description,
        "surfaceId": surface_id,
        "surfaceTitle": surface_title,
    }
    params.update(extra)
    return params


